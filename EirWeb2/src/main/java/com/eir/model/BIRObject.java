public class BIRObject {
	
	String birReportStatus;
	String birRequestDate;
	String birResponseDate;
	String birMessage;
	String birTats;
	public String getBirReportStatus() {
		return birReportStatus;
	}
	public void setBirReportStatus(String birReportStatus) {
		this.birReportStatus = birReportStatus;
	}
	public String getBirRequestDate() {
		return birRequestDate;
	}
	public void setBirRequestDate(String birRequestDate) {
		this.birRequestDate = birRequestDate;
	}
	public String getBirResponseDate() {
		return birResponseDate;
	}
	public void setBirResponseDate(String birResponseDate) {
		this.birResponseDate = birResponseDate;
	}
	public String getBirMessage() {
		return birMessage;
	}
	public void setBirMessage(String birMessage) {
		this.birMessage = birMessage;
	}
	public String getBirTats() {
		return birTats;
	}
	public void setBirTats(String birTats) {
		this.birTats = birTats;
	}
	
}
